#include <iostream>

using namespace std;

using ll = long long;

int main(void) {
    ios_base::sync_with_stdio(false);
    cin.tie(0); cout.tie(0);

    int t;
    cin >> t;
    
    while (t--) {
        ll n;
        cin >> n;
        
        cout << n * n << '\n';
    }
    
    return 0;
}
